package com.sumdu.petrenko.diplom.enums;

/**
 * Ролі користувачів у групах.
 * <p>
 * Цей енумератор визначає можливі ролі користувачів у контексті груп.
 * Ролі визначають рівень доступу та можливості користувача в групі.
 * Ієрархія ролей (від вищої до нижчої): OWNER → ADMIN → USER → PENDING.
 * </p>
 */
public enum UserGroupRole {
    /**
     * Роль власника групи.
     * <p>
     * Власник має найвищий рівень доступу. Має всі права адміністратора,
     * а також може видаляти групу та передавати права власника іншому користувачу.
     * У групі може бути лише один власник.
     * </p>
     */
    OWNER,

    /**
     * Роль адміністратора групи.
     * <p>
     * Адміністратор може керувати контентом групи, керувати користувачами
     * (додавати/видаляти/змінювати ролі, крім власника), та модерувати повідомлення.
     * У групі може бути кілька адміністраторів.
     * </p>
     */
    ADMIN,

    /**
     * Звичайна роль користувача в групі.
     * <p>
     * Користувач має базовий доступ до вмісту групи, може публікувати повідомлення
     * та брати участь в обговореннях відповідно до правил групи.
     * </p>
     */
    USER,

    /**
     * Користувач, який очікує на додавання до групи.
     * <p>
     * Ця роль призначається, коли користувач надіслав запит на вступ до приватної групи,
     * але ще не був прийнятий адміністратором або власником.
     * Користувачі з цією роллю не мають доступу до вмісту групи.
     * </p>
     */
    PENDING
}
